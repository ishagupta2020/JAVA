public class ForLoopEx {
    public static void main(String[] args) {
      for(int i=1;i<=20;i++){  //outer loop is for repetition
            int tableOf=i;
            for(int j=1;j<=10;j++){
                System.out.print(tableOf * j + " ");
            }
            System.out.println();
        }

    }
}
